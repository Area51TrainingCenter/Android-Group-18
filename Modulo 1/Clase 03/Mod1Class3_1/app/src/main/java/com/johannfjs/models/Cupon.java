public class Cupon {
    private String titulo;
    private String subtitulo;
    private String textoUno;
    private String textoDos;
    private String precioUno;
    private String precioDos;
    private String rutaImagen;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getSubtitulo() {
        return subtitulo;
    }

    public void setSubtitulo(String subtitulo) {
        this.subtitulo = subtitulo;
    }

    public String getTextoUno() {
        return textoUno;
    }

    public void setTextoUno(String textoUno) {
        this.textoUno = textoUno;
    }

    public String getTextoDos() {
        return textoDos;
    }

    public void setTextoDos(String textoDos) {
        this.textoDos = textoDos;
    }

    public String getPrecioUno() {
        return precioUno;
    }

    public void setPrecioUno(String precioUno) {
        this.precioUno = precioUno;
    }

    public String getPrecioDos() {
        return precioDos;
    }

    public void setPrecioDos(String precioDos) {
        this.precioDos = precioDos;
    }

    public String getRutaImagen() {
        return rutaImagen;
    }

    public void setRutaImagen(String rutaImagen) {
        this.rutaImagen = rutaImagen;
    }
}
